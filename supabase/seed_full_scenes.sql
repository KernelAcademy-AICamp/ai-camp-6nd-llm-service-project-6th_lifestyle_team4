-- ============================================================================
-- seed_full_scenes.sql
-- 각 카드의 한 줄 명대사가 포함된 "한 씬" 단위 전체 각본을
-- cards.scene_meta.full_scene 에 머지한다.
--
-- 적용 방법: Supabase SQL Editor 에 그대로 붙여넣어 실행.
-- jsonb || 연산자로 키를 덮어쓰므로 반복 실행 안전.
--
-- 풀 씬 텍스트는 모두 가공된 임의 시나리오 (저작권 우회용 패러프레이즈).
-- ============================================================================

