    fun classifyKeyword(kw: String): String {
        if (kw.isEmpty()) return UNCLASSIFIED
        OVERRIDE[kw]?.let { return it }
        for ((cat, subs) in RULES) {
            for (s in subs) if (kw.contains(s)) return cat
        }
        return UNCLASSIFIED
    }

    /** 카드 키워드 배열 → 닿는 주제 집합 (미분류 제외). */
    fun cardThemeSet(keywords: List<String>): Set<String> {
        val set = HashSet<String>()
        for (kw in keywords) {
            val c = classifyKeyword(kw)
            if (c != UNCLASSIFIED) set.add(c)
        }
        return set
    }
