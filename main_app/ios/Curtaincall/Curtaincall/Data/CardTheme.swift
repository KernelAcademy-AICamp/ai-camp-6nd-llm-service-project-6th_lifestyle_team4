    static func classifyKeyword(_ kw: String) -> String {
        if kw.isEmpty { return unclassified }
        if let o = override[kw] { return o }
        for (cat, subs) in rules {
            for s in subs where kw.contains(s) { return cat }
        }
        return unclassified
    }

    /// Card keywords → the set of themes they touch (excluding 미분류).
    static func cardThemeSet(_ keywords: [String]) -> Set<String> {
        var set = Set<String>()
        for kw in keywords {
            let c = classifyKeyword(kw)
            if c != unclassified { set.insert(c) }
        }
        return set
    }
